package com.javalec.ex;

public class student {
	private String name;
	private int age;
	private int grade;
	private int studentNum;
	
	public student() {
		
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getAge() {
		return age;
	}
	
	public void setAge(int age) {
		this.age = age;
	}
	
	public int getGrade() {
		return grade;
	}
	
	public void setGrade(int grade) {
		this.grade = grade;
	}
	
	public int studentNum() {
		return studentNum;
	}
	
	public void stdentNum(int studentNum) {
		this.studentNum = studentNum;
	}

}
